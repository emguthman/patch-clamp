function [axProperties] = setAx(axProperties)
%basic tuning of axes to the properties I like -- emg 6/8/18
axProperties.Box = 'off'; %takes off box
axProperties.YColor = 'k'; %colors axes black
axProperties.XColor = 'k'; 
axProperties.LineWidth = 2; %linewidth 1
axProperties.TickDir ='out'; %turns ticks outward

end